# creating a list
friends = ["Des", "Jam", "Bea", "Lena"]

# You can put any kind of data type in the list/array

print(friends)

# grabbing a certain name depending on the index
print(friends[1])
 
# Access elements from back of the list
print(friends[-2])


# selecting the certain elements up to 
print(friends[1:])

# change an element
friends[1] = "Karen"
print(friends)